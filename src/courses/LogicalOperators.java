package courses;

public class LogicalOperators {

	public static void main(String[] args) {
 
		boolean x= 99>100  , y = 100>99 ;
		
		
		//logical operator &&
		
		System.out.println( "what's the result between 99>100 && 100>99 " + (x && y) );
		
		
	//logical operator ||
		
		System.out.println( "what's the result between 99>100 || 100>99 " + (x || y) );
		
		
		
		
		//logical operator !
		
				System.out.println( "what's the result between 99>100 ! 100>99 " + (x != y) );
				
				

		
		
		
		
		
		
		
		
		
	}

}
